<template>
  <div>
    <h1>Register</h1>
    <input
      type="email"
      name="email"
      placeholder="email"
      v-model="email">
      <input
        type="password"
        name="password"
        v-model="password">
      <button
        @click="onRegisterUser">Register</button>
  </div>
</template>

<script>
import AuthenticationService from '@/services/AuthenticationService'
export default {
  data () {
    return {
      email: '',
      password: ''
    }
  },
  methods: {
    async onRegisterUser() {
      const response = await AuthenticationService.register({
        email: this.email,
        password: this.password
      })
      console.log(response.data)
    }
  }
}
</script>
